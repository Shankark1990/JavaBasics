package datatypes;

public class ShortTypeDemo {
    public static void main(String[] args) {
        short sh=180;
        byte b = (byte) sh;

        System.out.println(b);

        double db=13.30;
        float fl=(float) db;

        System.out.println(fl);

        Short s1=(byte)130;
        System.out.println(s1);
        for(;;){
            System.out.println("Hello");
        }
    }
}
